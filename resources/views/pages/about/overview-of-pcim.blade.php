<p>Because Next Gen PET was developed to be explicitly aligned with the Next Generation Science Standards (NGSS), it is a model curriculum for these standards. It exemplifies three-dimensional learning with core content ideas in the physical sciences, as well as scientific practices and crosscutting concepts. </p>

    <p>The NGSS alignment is nearly complete in the core materials for Next Gen PET. However, with respect to NGSS Practice 3, “Planning and Carrying Out Investigations,” even though students have many opportunities to carry out investigations throughout the curriculum, they do not have opportunities to plan or design their own investigation. With that in mind, the goal of this optional set of activities is to add activities that support this entire practice. The design is meant to be consistent with the existing materials, but with as much flexibly as possible. The materials as presented below are designed for the Studio-style setting and can be used freely by just downloading them from this site.  </p>

    <p>The materials were written with the support of the National Science Foundation grant NSF DUE-1611738 by Dr. Paul Miller at West Virginia University. If you choose to use these activities, suggestions and feedback are welcome. </p>
