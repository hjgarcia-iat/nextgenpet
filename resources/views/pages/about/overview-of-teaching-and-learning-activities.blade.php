    <section class="html-content">
        <p>Teaching and Learning activities help Next Gen PET students make explicit connections between their own learning; the learning and teaching of children in elementary school; and the core ideas, science and engineering practices, and crosscutting concepts of the NGSS. Each content module includes one or more Teaching and Learning activity (both in-class and extension) aligned with the module’s content. More detail on their structure, and a list of the activities, is included below. The first few Teaching and Learning activities are intended to introduce this part of the curricula to students; they are not aligned with any specific content module. </p>
        <p>
        The Teaching and Learning activities can be used in university science courses for future teachers, science methods courses, or workshops for in-service teachers. A university science course could include the Teaching and Learning activities throughout the course in conjunction with the content modules. In education courses for future teachers or workshops for in-service teachers, the Teaching and Learning activities can be used alone or in conjunction with a subset of the content lessons. For example, an instructor may want to engage his or her students in focused deep learning of a specific content area as part of a methods course, allowing his or her students to experience learning in the way expected by the NGSS. In that case, a productive scenario would be to choose one of the content units or modules (e.g., the Magnetism unit) and do all of the content activities and the associated Teaching and Learning activities.
        </p>
        <p>
        Another option is to complete only the teaching and learning activities across multiple units. Because the cluster of Teaching and Learning activities associated with each content module focuses on specific practices or on the crosscutting concepts, this second option would give pre-service and in-service teachers targeted learning experiences with multiple practices. While the Teaching and Learning activities build on the same physical science content as the content modules, they do not depend on having done the content activities. Faculty using the Teaching and Learning activities without the associated content units may want to review the physical science content. The Teaching and Learning activities challenge Next Gen PET students to identify how children are engaging in scientific practices through watching videos of children engaging in science and engineering lessons. The longer extensions provide opportunities for students to practice identifying children's’ productive science ideas through interviews, and practice engaging children in the science and engineering practices.
        </p>
        <p>
        Within each module, the Teaching and Learning activities are organized into a cluster of coherent in-class and out-of-class extension activities. Each content cluster includes video analysis of teachers engaging children in NGSS-aligned instruction related to the corresponding content module; focused exploration of one practice and one crosscutting concept; and an extended out-of-class activity to interview children about their ideas about the content area or lead them through a small learning activity. These extended out-of-class activities are scaffolded so that undergraduates’ experiences become more complex over time. For example, in the Magnetism and Static Electricity Module, the Teaching and Learning activity is a guided interview about magnetism. Next Gen PET students are guided to elicit children’s ideas about magnetism through provided questions. (It is assumed that students can interview a child who is a relative, child of a friend, or participant at a fieldwork site.) Later in the curriculum, during the Interactions and Forces Module, an extended out-of-class activity guides undergraduates through the teaching of a small investigation related to forces, allowing them to gain experience facilitating instruction through the practices of the NGSS.
        </p>
        
        <h3>In-Class Lessons (LC) & Activities (SC)</h3>
        
        <p>
            Lessons are designed for lecture style classes and activities are designed for studio-style classes. Each lesson and activity is listed below along with a brief summary, the NGSS alignment, estimated class time needed, and the pre-requisite TL extensions (if any). Below each description is a suggestion for when you should conduct the in-class lesson/activity. There is a list of the NGSS science and engineering practices and the crosscutting concepts following the tables below.
        </p>
        <p>
            Note that several of the extensions are group projects involving the interviewing of children. These projects typically take 2-3 weeks from initial assignment to the time the final individual and group reports are turned in. Although the physical science content (e.g., magnetism, energy, light, etc.) will probably be associated with whatever content unit your class is doing at the time you assign the interview extension, by the time they are done the class will (probably) be on a different unit. Thus, the follow-up lessons/activities, where students reflect on their interactions with the children they interviewed, will not be connected to your current unit.
        </p>
    
